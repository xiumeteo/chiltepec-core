package com.xiumeteo.chiltepec.test.utils;

/**
 * Created on 13/10/2014.
 */
public class SimpleNullObject {

    private String string = null;

    private Comparable comparable = null;

    private Integer integer = null;

    public String getString() {
        return string;
    }

    public void setString(final String string) {
        this.string = string;
    }

    public Comparable getComparable() {
        return comparable;
    }

    public void setComparable(final Comparable comparable) {
        this.comparable = comparable;
    }

    public Integer getInteger() {
        return integer;
    }

    public void setInteger(final Integer integer) {
        this.integer = integer;
    }
}
